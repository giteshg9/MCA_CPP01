void bubble(int arr[],int size)
{
    for(int i = size -1; i >= 0; i--)
    {
        int hasSwaped = 0;
        for(int j = 0; j <= i -1; j++)
        {
            if(arr[j], arr[j+1]);
            hasSwaped = 1;
        }
    }
    if(hasSwapped == 0)break;
}
